    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        DataSource dataSource = new DataSource();
        PreparedStatement ps = null;
        
        try{
            DefaultTableModel model = (DefaultTableModel) table.getModel();
            for (int i = table.getRowCount() - 1; i >= 0; i--) {
                model.removeRow(i);
            }
            
        String SQL = "SELECT * FROM entidades WHERE (id = ? OR ? = '') AND (nome LIKE ? OR ? = '') AND tipo = ? AND (representante LIKE ? OR ? = '') AND (email LIKE ? OR ? = '') AND (telefone LIKE ? OR ? = '');";

        try {
            // para mandar como uma instrução, precisa usar o PreparedStatement
            // traduz o comando SQL para execução
            ps = dataSource.getConnection().prepareStatement(SQL);
            ps.setString(1, codigoPesquisa.getText());
            ps.setString(2, codigoPesquisa.getText());
            ps.setString(3, "%" + nomePesquisa.getText() + "%");
            ps.setString(4, nomePesquisa.getText());
            ps.setString(5, "Fornecedor");
            ps.setString(6, "%" + representantePesquisa.getText() + "%");
            ps.setString(7, representantePesquisa.getText());
            ps.setString(8, "%" + emailPesquisa.getText() + "%");
            ps.setString(9, emailPesquisa.getText());
            ps.setString(10, "%" + (telefonePesquisa.getValue() == null ? "" : telefonePesquisa.getValue()) + "%");
            ps.setString(11, "%" + (telefonePesquisa.getValue() == null ? "" : telefonePesquisa.getValue()) + "%");
                
            } catch (SQLException ex) {
                Logger.getLogger(JIFFornecedores.class.getName()).log(Level.SEVERE, null, ex);
            }

            // executa a consulta no banco
            ResultSet rs = null;
            try {
                rs = ps.executeQuery();
            } catch (SQLException ex) {
                Logger.getLogger(JIFFornecedores.class.getName()).log(Level.SEVERE, null, ex);
            }
            
            // cria a lista de resultados trazidos da tabela
            ArrayList<Entidades> lista = new ArrayList<>();
            
            // enquanto tiverem registros no ResultSet (rs), 
            // vai adicionando na lista
            while(rs.next()){
                // cria objeto de municipio
                // cada cidade é um objeto
                Entidades user = new Entidades();
               
                // adiciona o objeto (registro) na lista (arraylist)
                lista.add(user);

                model.addRow(new Object[]{rs.getInt("id"), rs.getString("nome"), rs.getString("telefone"), rs.getString("email"), rs.getString("cnpj"), rs.getString("uf"), rs.getString("complemento"), rs.getString("endereco"), rs.getString("bairro"), rs.getString("cep"), rs.getString("cidade"), rs.getString("representante") , rs.getString("telefoneRepresentante")});
            }
            
            try {
                // fecha o statement e o datasource
                ps.close();
            } catch (SQLException ex) {
                Logger.getLogger(JIFFornecedores.class.getName()).log(Level.SEVERE, null, ex);
            }
            dataSource.closeDataSource();
        }   catch (SQLException ex) {
                Logger.getLogger(JIFFornecedores.class.getName()).log(Level.SEVERE, null, ex);
            }        
    }//GEN-LAST:event_jButton2ActionPerformed

