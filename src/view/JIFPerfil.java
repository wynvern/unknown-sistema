    private static byte[] getBytesFromFile(File file) {
        byte[] imageData = null;
        try (FileInputStream fis = new FileInputStream(file)) {
            imageData = new byte[(int) file.length()];
            fis.read(imageData);
        } catch (Exception e) {
            e.printStackTrace();
        }
        return imageData;
    }
    

