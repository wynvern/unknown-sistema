    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        FileNameExtensionFilter imageFilter = new FileNameExtensionFilter(
                "Image files", "jpg", "jpeg", "png", "gif", "bmp");
        fileChooser.setFileFilter(imageFilter);
        
        int result = fileChooser.showOpenDialog(null);
        if (result != JFileChooser.APPROVE_OPTION) {
            return;
        }
        

        byte[] imageData = getBytesFromFile(fileChooser.getSelectedFile().getAbsoluteFile());
        
        ImageIcon imageIcon = new ImageIcon(imageData);
        Image imageIcoResized = imageIcon.getImage().getScaledInstance(80, 80, Image.SCALE_SMOOTH);
        imageIcon = new ImageIcon(imageIcoResized);
        imageLabel.setIcon(imageIcon);
        
        imageProfile = imageData;
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        DataSource dataSource = new DataSource();
        Connection con = dataSource.getConnection();
        PreparedStatement ps = null;
        
        String nomeABC = usuarioLogado.getNome();
        String emailABC = usuarioLogado.getEmail();
        String telefoneABC = usuarioLogado.getTelefone();
        String senhaABC = usuarioLogado.getSenha();
        
        if (nome.getText().length() > 50) {
            JOptionPane.showMessageDialog(null, "O nome n√£o pode ser maior que 60 caract√©res.", "Aviso", JOptionPane.INFORMATION_MESSAGE);
            return;
        }

        try{
            String SQL = "UPDATE usuarioSistema SET imagemPerfil=?,nome=?,senha=?,email=?,telefone=? WHERE id=?";

            ps = con.prepareStatement(SQL);

            ps.setBytes(1, imageProfile);
            

            if (nome.getText().equals("")) ps.setString(2, usuarioLogado.getNome());
            else {
                ps.setString(2, nome.getText());
                nomeABC = nome.getText();
            }
            
            if (email.getText().equals("")) ps.setString(4, usuarioLogado.getEmail());
            else {
                ps.setString(4, email.getText());
                emailABC = email.getText();
            }
                        
            if (telefone.getText().equals("")) ps.setString(5, usuarioLogado.getTelefone());
            else {
                ps.setString(5, telefone.getText());
                telefoneABC = telefone.getText();
            }
            
            if (novaSenha.getText().equals("") || novaSenhaConfirma.getText().equals("")) {
                ps.setString(3, usuarioLogado.getSenha());
            } else if (novaSenhaConfirma.getText().equals(novaSenha.getText())) {
                ps.setString(3, novaSenha.getText());
                senhaABC = novaSenha.getText();
            }
            
            ps.setInt(6, usuarioLogado.getId());

            // executa a inser√ß√£o no banco
            ps.executeUpdate();
            ps.close();
            
        }
        catch (SQLException ex){
            //System.err.println("Erro ao salvar os dados "+ex.getMessage());
            JOptionPane.showMessageDialog(null,"Erro ao alterar!\n"+ex);
        }
        finally{
            // fecha o statement e o datasource
            dataSource.closeDataSource();
        }
        
        usuarioLogado.setImagemPerfil(imageProfile);
        usuarioLogado.setNome(nomeABC);
        usuarioLogado.setEmail(emailABC);
        usuarioLogado.setTelefone(telefoneABC);
        usuarioLogado.setSenha(senhaABC);
        updateProfileStuff();
        this.dispose();
        JFSistema.perfilOpened = false;
    }//GEN-LAST:event_jButton1ActionPerformed

